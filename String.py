# This is a string as shown below

Text = "Hello"
Text2 = "THERE"

print(Text)

# Accessing an element  in a string
print(Text[1])

# size of a string... this is the number of characters in a given text
print(len(Text2))

# Modifying a string
# This is changing the case ie upper and lower case

print(Text.upper())
print(Text2.lower())

# String concatenation...... combining 2 or more strings
print(Text + Text2)
print(Text + "" + Text2)
